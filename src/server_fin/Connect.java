package server_fin;

public class Connect {

	public String name;
	public String pass;
	public String action;
	public String newSecrets;
	public String admin;

	public Connect(String name, String pass, String action, String newSecrets, String admin) {

		this.name = name;
		this.pass = pass;
		this.action = action;
		this.newSecrets = newSecrets;
		this.admin = admin;
	}

}
